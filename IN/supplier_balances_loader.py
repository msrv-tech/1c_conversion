def load_supplier_balances(
    sqlite_db_file: str,
    com_object,
    mode: str = "test",
    process_func=None,
    filters_db: str | None = None,
) -> bool:
    verbose_print("=" * 80)
    verbose_print("ЗАГРУЗКА ОСТАТКОВ ВЗАИМОРАСЧЕТОВ С ПОСТАВЩИКАМИ")
    verbose_print("=" * 80)

    if not com_object:
        return False

    # В 1С УПП собираем все субсчета счета 60
    # Группируем их по типу: расчеты (Кт) и авансы (Дт)
    
    results = []
    
    # 1. Расчеты (Кт)
    settlement_codes = ["60.01", "60.03", "60.04", "60.04.1", "60.04.2", "60.04.3", "60.21", "60.31"]
    verbose_print(f"Шаг 1: Загрузка кредитовых остатков (расчеты: {', '.join(settlement_codes)})...")
    try:
        accounts_list = com_object.NewObject("Массив")
        for code in settlement_codes:
            acc = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(code)
            if not acc.Пустая():
                accounts_list.Add(acc)
        
        if accounts_list.Количество() > 0:
            params = {
                "Счета": accounts_list,
                "ОстатокДт": False,
                "ПоВсемОрганизациям": True,
                "Организация": com_object.NewObject("Массив")
            }
        
        rows = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
        verbose_print(f"  Найдено строк (Кт): {len(rows)}")
        results.extend(rows)
        
    except Exception as e:
        verbose_print(f"  Ошибка при загрузке Кт остатков: {e}")

    # 2. Авансы (Дт)
    advance_codes = ["60.02", "60.05", "60.05.1", "60.05.2", "60.05.3", "60.22", "60.32"]
    verbose_print(f"Шаг 2: Загрузка дебетовых остатков (авансы: {', '.join(advance_codes)})...")
    try:
        accounts_list = com_object.NewObject("Массив")
        for code in advance_codes:
            acc = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(code)
            if not acc.Пустая():
                accounts_list.Add(acc)
        
        if accounts_list.Количество() > 0:
            params = {
                "Счета": accounts_list,
                "ОстатокДт": True,
                "ПоВсемОрганизациям": True,
                "Организация": com_object.NewObject("Массив")
            }
        
        rows = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
        verbose_print(f"  Найдено строк (Дт): {len(rows)}")
        results.extend(rows)
        
    except Exception as e:
        verbose_print(f"  Ошибка при загрузке Дт остатков: {e}")

    if not results:
        verbose_print("Остатки не найдены.")
        return True

    verbose_print(f"Всего собрано строк: {len(results)}")

    # Убираем дубликаты: берём строку с максимальным СФ (по дате, затем по номеру) на группу (орг, контрагент, договор, счёт, документ, сделка)
    def _row_key(r):
        return (
            str(r.get("Организация_UUID") or ""),
            str(r.get("Контрагент_UUID") or ""),
            str(r.get("Договор_UUID") or ""),
            str(r.get("Счет_UUID") or ""),
            str(r.get("Документ_UUID") or ""),
            str(r.get("Сделка_UUID") or ""),
        )

    def _sf_sort_key(r):
        dt = r.get("Документ_ДатаВходящаяСФ")
        num = str(r.get("Документ_НомерВходящийСФ") or "")
        if dt is None:
            return ("", num)
        return (str(dt), num)

    seen: dict = {}
    for r in results:
        k = _row_key(r)
        if k not in seen:
            seen[k] = []
        seen[k].append(r)
    results = [max(group, key=_sf_sort_key) for group in seen.values()]
    verbose_print(f"После устранения дубликатов СФ: {len(results)} строк")

    # Добавляем row_uuid для update_reference_types (уникальность по ключевым полям)
    import hashlib
    for row in results:
        parts = [
            str(row.get("Организация_UUID", "") or ""),
            str(row.get("Контрагент_UUID", "") or ""),
            str(row.get("Договор_UUID", "") or ""),
            str(row.get("Счет_UUID", "") or ""),
            str(row.get("Документ_UUID", "") or ""),
            str(row.get("Сделка_UUID", "") or ""),
        ]
        row["row_uuid"] = hashlib.sha256("|".join(parts).encode()).hexdigest()

    if not ensure_database_exists(sqlite_db_file):
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        return False

    try:
        # Очищаем таблицу перед загрузкой, так как мы всегда переписываем остатки целиком
        cursor = connection.cursor()
        cursor.execute(f"DROP TABLE IF EXISTS {TABLE_NAME}")
        cursor.execute(f"CREATE TABLE {TABLE_NAME} (row_uuid TEXT PRIMARY KEY, Организация TEXT, Контрагент TEXT, Договор TEXT, Счет TEXT, Документ TEXT, Документ_Номер TEXT, Документ_Дата TEXT, Документ_НомерВходящий TEXT, Документ_ДатаВходящая TEXT, Документ_НомерВходящийСФ TEXT, Документ_ДатаВходящаяСФ TEXT, Документ_Сумма REAL, Документ_Валюта TEXT, Сделка TEXT, Валюта TEXT, СуммаКонечныйОстатокБУ REAL, СуммаВзаиморасчетовОстаток REAL)")
        connection.commit()

        # Обрабатываем ссылочные поля
        rows = process_reference_fields(results, REFERENCE_COLUMNS)

        from tools.onec_connector import upsert_rows
        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "row_uuid": "TEXT PRIMARY KEY",
                "Организация": "TEXT",
                "Контрагент": "TEXT",
                "Договор": "TEXT",
                "Счет": "TEXT",
                "Документ": "TEXT",
                "Документ_Номер": "TEXT",
                "Документ_Дата": "TEXT",
                "Документ_НомерВходящий": "TEXT",
                "Документ_ДатаВходящая": "TEXT",
                "Документ_НомерВходящийСФ": "TEXT",
                "Документ_ДатаВходящаяСФ": "TEXT",
                "Документ_Сумма": "REAL",
                "Документ_Валюта": "TEXT",
                "Сделка": "TEXT",
                "Валюта": "TEXT",
                "СуммаКонечныйОстатокБУ": "REAL",
                "СуммаВзаиморасчетовОстаток": "REAL",
            }
        )
        verbose_print(f"Сохранено в SQLite: {saved} записей.")
        return True
    finally:
        connection.close()

