# Маппинг: 10.11 → МЦ.02, 10.09 → МЦ.04
ACCOUNT_TO_TARGET = {
    "10.11": "МЦ.02",
    "10.09": "МЦ.04",
}


def load_parties_mc_balances(sqlite_db_file: str, com_object, mode: str = "test") -> bool:
    verbose_print("=" * 80)
    verbose_print("ЗАГРУЗКА ОСТАТКОВ МЦ.02, МЦ.04 (ПАРТИИ МАТЕРИАЛОВ В ЭКСПЛУАТАЦИИ)")
    verbose_print("=" * 80)

    if com_object is None:
        verbose_print("Ошибка: com_object обязателен")
        return False

    # Счета 10.11 и 10.09 в плане счетов источника
    accounts_list = com_object.NewObject("Массив")
    for code in ACCOUNT_TO_TARGET:
        account_ref = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(code)
        if account_ref.Пустая():
            verbose_print(f"Счёт {code} не найден в плане счетов источника.")
            continue
        accounts_list.Add(account_ref)

    if accounts_list.Количество() == 0:
        verbose_print("Ни один из счетов (10.11, 10.09) не найден в плане счетов источника.")
        return False

    try:
        params = {"Счета": accounts_list}
        rows = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
    except Exception as error:
        verbose_print(f"Ошибка выполнения запроса: {error}")
        import traceback
        verbose_print(traceback.format_exc())
        return False

    if not rows:
        verbose_print("Не удалось получить записи остатков.")
        return True

    verbose_print(f"Получено записей: {len(rows)}")

    rows = process_reference_fields(rows, REFERENCE_COLUMNS)

    for row in rows:
        # TargetAccountCode по коду счёта: 10.11 → МЦ.02, 10.09 → МЦ.04
        acc_code = str(row.get("СчетУчетаКод") or row.get("СчетУчета_Представление") or "").strip()
        row["TargetAccountCode"] = ACCOUNT_TO_TARGET.get(acc_code, "МЦ.02")
        row.pop("СчетУчетаКод", None)

        # Числовые поля
        val = row.pop("КоличествоОстаток", None)
        try:
            row["Quantity"] = float(val) if val is not None and val != "" else 0.0
        except (TypeError, ValueError):
            row["Quantity"] = 0.0

        val = row.pop("СтоимостьОстаток", None)
        try:
            row["Amount"] = float(val) if val is not None and val != "" else 0.0
        except (TypeError, ValueError):
            row["Amount"] = 0.0

        val = row.pop("ПогашеннаяСтоимостьОстаток", None)
        try:
            row["WrittenOffAmount"] = float(val) if val is not None and val != "" else 0.0
        except (TypeError, ValueError):
            row["WrittenOffAmount"] = 0.0

    for row in rows:
        row["uuid"] = str(uuid.uuid4())

    if not ensure_database_exists(sqlite_db_file):
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        return False

    try:
        cursor = connection.cursor()
        cursor.execute(f'DROP TABLE IF EXISTS "{TABLE_NAME}"')
        connection.commit()

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "uuid": "TEXT PRIMARY KEY",
                "TargetAccountCode": "TEXT",
                "Quantity": "REAL",
                "Amount": "REAL",
                "WrittenOffAmount": "REAL",
                "ДокументПередачиНомер": "TEXT",
            },
        )
        verbose_print(f"Сохранено строк: {saved}")
    finally:
        connection.close()

    return True


if __name__ == "__main__":
    target = os.getenv("TARGET_1C", "source")
