# Коды счетов 20 по правилу ВводНачальныхОстатков_20
ACCOUNT_CODES = [
    "20.01.1", "20.01.2", "20.01.3", "20.01.4",
    "20.02.1", "20.02.2", "20.02.3", "20.02.4",
]


def load_account_20_balances(sqlite_db_file: str, com_object, mode: str = "test") -> bool:
    verbose_print("=" * 80)
    verbose_print("ЗАГРУЗКА ОСТАТКОВ ПО СЧЁТУ 20 (ОСНОВНОЕ ПРОИЗВОДСТВО)")
    verbose_print("=" * 80)

    if com_object is None:
        verbose_print("Ошибка: com_object обязателен")
        return False

    # Собираем ссылки на счета БУ по кодам
    accounts_list = com_object.NewObject("Массив")
    found_any = False
    for code in ACCOUNT_CODES:
        acc_ref = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(code)
        if acc_ref and not acc_ref.Пустая():
            accounts_list.Add(acc_ref)
            found_any = True

    if not found_any:
        verbose_print(f"Не найдено ни одного счёта из списка: {ACCOUNT_CODES}")
        return False

    verbose_print(f"Найдено счетов БУ: {accounts_list.Количество()}")

    # Счёта НУ с теми же кодами (допущение 1:1)
    accounts_list_nu = com_object.NewObject("Массив")
    for code in ACCOUNT_CODES:
        try:
            acc_ref = com_object.ПланыСчетов.Налоговый.НайтиПоКоду(code)
            if acc_ref and not acc_ref.Пустая():
                accounts_list_nu.Add(acc_ref)
        except Exception:
            pass
    if accounts_list_nu.Количество() == 0:
        verbose_print("Предупреждение: счета НУ не найдены, налоговые суммы будут 0")

    try:
        params = {"СписокСчетов": accounts_list, "СписокСчетовНУ": accounts_list_nu}
        rows = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
    except Exception as error:
        verbose_print(f"Ошибка выполнения запроса: {error}")
        import traceback
        verbose_print(traceback.format_exc())
        return False

    if not rows:
        verbose_print("Нет остатков по счёту 20.")
        return True

    verbose_print(f"Получено записей: {len(rows)}")

    # Переименование числовых полей для БД
    for row in rows:
        if "Количество" in row:
            val = row.pop("Количество")
            try:
                row["Quantity"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["Quantity"] = 0.0
        if "Сумма" in row:
            val = row.pop("Сумма")
            try:
                row["Amount"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["Amount"] = 0.0
        if "СуммаКт" in row:
            val = row.pop("СуммаКт")
            try:
                row["AmountCredit"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["AmountCredit"] = 0.0
        if "ВалютнаяСумма" in row:
            val = row.pop("ВалютнаяСумма")
            try:
                row["CurrencyAmount"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["CurrencyAmount"] = 0.0
        if "СуммаНУ" in row:
            val = row.pop("СуммаНУ")
            try:
                row["AmountNU"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["AmountNU"] = 0.0
        if "СуммаПР" in row:
            val = row.pop("СуммаПР")
            try:
                row["AmountPR"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["AmountPR"] = 0.0
        if "СуммаВР" in row:
            val = row.pop("СуммаВР")
            try:
                row["AmountVR"] = float(val) if val is not None and val != "" else 0.0
            except (TypeError, ValueError):
                row["AmountVR"] = 0.0

    # Отсеиваем строки с пустыми суммами — должна быть заполнена хотя бы одна
    def _has_any_sum(r):
        return (
            (r.get("Amount") or 0) != 0
            or (r.get("AmountCredit") or 0) != 0
            or (r.get("CurrencyAmount") or 0) != 0
            or (r.get("Quantity") or 0) != 0
            or (r.get("AmountNU") or 0) != 0
            or (r.get("AmountPR") or 0) != 0
            or (r.get("AmountVR") or 0) != 0
        )

    rows = [r for r in rows if _has_any_sum(r)]
    verbose_print(f"После отбора по суммам: {len(rows)} записей")

    if not rows:
        verbose_print("Нет строк с заполненными суммами.")
        return True

    rows = process_reference_fields(rows, REFERENCE_COLUMNS)

    for row in rows:
        row["uuid"] = str(uuid.uuid4())

    if not ensure_database_exists(sqlite_db_file):
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        return False

    try:
        cursor = connection.cursor()
        cursor.execute(f'DROP TABLE IF EXISTS "{TABLE_NAME}"')
        connection.commit()

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "uuid": "TEXT PRIMARY KEY",
                "Quantity": "REAL",
                "Amount": "REAL",
                "AmountCredit": "REAL",
                "CurrencyAmount": "REAL",
                "AmountNU": "REAL",
                "AmountPR": "REAL",
                "AmountVR": "REAL",
            },
        )
        verbose_print(f"Сохранено строк: {saved}")
    finally:
        connection.close()

    return True


