def load_rbp_balances(
    sqlite_db_file: str,
    com_object,
    mode: str = "test",
    process_func=None,
    filters_db: str | None = None,
) -> bool:
    verbose_print("=" * 80)
    verbose_print("ЗАГРУЗКА ОСТАТКОВ РБП (СЧЕТА 97, 76.19, 76.09)")
    verbose_print("=" * 80)

    if not com_object:
        verbose_print("Ошибка: com_object обязателен")
        return False

    accounts_list = com_object.NewObject("Массив")
    for code in ACCOUNT_CODES:
        acc = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(code)
        if not acc.Пустая():
            accounts_list.Add(acc)

    if accounts_list.Количество() == 0:
        verbose_print("Счета 97, 76.19, 76.09 не найдены в плане счетов.")
        return True

    accounts_list_nu = com_object.NewObject("Массив")
    for code in ACCOUNT_CODES_NU:
        try:
            acc = com_object.ПланыСчетов.Налоговый.НайтиПоКоду(code)
            if acc and not acc.Пустая():
                accounts_list_nu.Add(acc)
        except Exception:
            pass

    verbose_print(f"Загрузка остатков (счета: {len(ACCOUNT_CODES)}, найдено: {accounts_list.Количество()}, НУ: {accounts_list_nu.Количество()})...")

    try:
        params = {"Счета": accounts_list, "СчетаНУ": accounts_list_nu}
        results = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
        verbose_print(f"Найдено строк: {len(results)}")
    except Exception as e:
        verbose_print(f"Ошибка при загрузке остатков: {e}")
        import traceback
        verbose_print(traceback.format_exc())
        return False

    if not results:
        verbose_print("Остатки не найдены.")
        return True

    # Фильтр 76.19 по ВидРБП в Python
    from tools.chart_of_accounts_mapper import extract_account_code

    rbp_uuids = set()
    filtered = []
    for row in results:
        acc_pres = (row.get("Счет_Представление") or "").strip()
        acc_code = extract_account_code(acc_pres) or (acc_pres.split()[0] if acc_pres else "")
        vid_rbp = (row.get("ВидРБП_Представление") or "").strip()
        if acc_code == "76.19" and vid_rbp != ВИД_РБП_ПО_НА_76:
            continue
        filtered.append(row)
        # Собираем UUID РБП для filters_db (счета 97 и 76.19)
        sub1_uuid = (row.get("Субконто1_UUID") or "").strip()
        sub2_uuid = (row.get("Субконто2_UUID") or "").strip()
        sub1_type = (row.get("Субконто1_Тип") or "").lower()
        sub2_type = (row.get("Субконто2_Тип") or "").lower()
        if "расходыбудущихпериодов" in sub1_type and sub1_uuid and sub1_uuid != "00000000-0000-0000-0000-000000000000":
            rbp_uuids.add(sub1_uuid)
        if "расходыбудущихпериодов" in sub2_type and sub2_uuid and sub2_uuid != "00000000-0000-0000-0000-000000000000":
            rbp_uuids.add(sub2_uuid)

    # Отсеиваем строки с пустыми суммами — должна быть заполнена хотя бы одна
    def _has_any_sum(r):
        return (
            (r.get("СуммаДт") or 0) != 0
            or (r.get("СуммаКт") or 0) != 0
            or (r.get("СуммаНУ") or 0) != 0
            or (r.get("СуммаПР") or 0) != 0
            or (r.get("СуммаВР") or 0) != 0
        )

    filtered = [r for r in filtered if _has_any_sum(r)]
    results = filtered
    verbose_print(f"После фильтра 76.19 по ВидРБП и отбора по суммам: {len(results)} строк")

    for i, row in enumerate(results):
        parts = [
            str(row.get("Организация_UUID", "") or ""),
            str(row.get("Счет_UUID", "") or ""),
            str(row.get("Субконто1_UUID", "") or ""),
            str(row.get("Субконто2_UUID", "") or ""),
            str(row.get("Счет_Представление", "") or ""),
            str(row.get("СуммаДт", "") or ""),
            str(row.get("СуммаКт", "") or ""),
            str(i),
        ]
        row["row_uuid"] = hashlib.sha256("|".join(parts).encode()).hexdigest()

    rows = process_reference_fields(results, REFERENCE_COLUMNS)

    if filters_db and rbp_uuids:
        from tools.filters_manager import add_catalog_uuids
        added = add_catalog_uuids(filters_db, "prepaid_expenses", rbp_uuids)
        verbose_print(f"Добавлено UUID РБП в filters_db: {added}")

    if not ensure_database_exists(sqlite_db_file):
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        return False

    try:
        cursor = connection.cursor()
        cursor.execute(f'DROP TABLE IF EXISTS "{TABLE_NAME}"')
        cursor.execute(f"""
            CREATE TABLE "{TABLE_NAME}" (
                row_uuid TEXT PRIMARY KEY,
                Организация TEXT,
                Счет TEXT,
                Субконто1 TEXT,
                Субконто2 TEXT,
                Субконто3 TEXT,
                ПодразделениеОрганизации TEXT,
                Валюта TEXT,
                ВидРБП_Представление TEXT,
                СуммаДт REAL,
                СуммаКт REAL,
                СуммаНУ REAL,
                СуммаПР REAL,
                СуммаВР REAL
            )
        """)
        connection.commit()

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "row_uuid": "TEXT PRIMARY KEY",
                "Организация": "TEXT",
                "Счет": "TEXT",
                "Субконто1": "TEXT",
                "Субконто2": "TEXT",
                "Субконто3": "TEXT",
                "ПодразделениеОрганизации": "TEXT",
                "Валюта": "TEXT",
                "ВидРБП_Представление": "TEXT",
                "СуммаДт": "REAL",
                "СуммаКт": "REAL",
                "СуммаНУ": "REAL",
                "СуммаПР": "REAL",
                "СуммаВР": "REAL",
            },
        )
        verbose_print(f"Сохранено в SQLite: {saved} записей.")
        return True
    finally:
        connection.close()


