COLUMNS = [
    "Организация", "Организация_Представление", "Организация_UUID", "Организация_Тип",
    "Счет", "Счет_Представление", "Счет_UUID", "Счет_Тип",
    "Субконто1", "Субконто1_Представление", "Субконто1_UUID", "Субконто1_Тип",
    "Субконто2", "Субконто2_Представление", "Субконто2_UUID", "Субконто2_Тип",
    "Субконто3", "Субконто3_Представление", "Субконто3_UUID", "Субконто3_Тип",
    "ПодразделениеОрганизации", "ПодразделениеОрганизации_Представление", "ПодразделениеОрганизации_UUID", "ПодразделениеОрганизации_Тип",
    "Валюта", "Валюта_Представление", "Валюта_UUID", "Валюта_Тип",
    "ВидРБП_Представление",
    "СуммаДт", "СуммаКт",
]

REFERENCE_COLUMNS = [
    "Организация", "Счет", "Субконто1", "Субконто2", "Субконто3",
    "ПодразделениеОрганизации", "Валюта",
]


def load_account_01301_rbp_balances(
    sqlite_db_file: str,
    com_object,
    mode: str = "test",
    process_func=None,
) -> bool:
    verbose_print("=" * 80)
    verbose_print("ЗАГРУЗКА ОСТАТКОВ СЧЁТ 013.01 (СУБКОНТО1 = РБП)")
    verbose_print("=" * 80)

    if not com_object:
        verbose_print("Ошибка: com_object обязателен")
        return False

    acc = com_object.ПланыСчетов.Хозрасчетный.НайтиПоКоду(ACCOUNT_CODE)
    if acc.Пустая():
        verbose_print(f"Ошибка: счёт {ACCOUNT_CODE} не найден в плане счетов хозрасчётного учёта.")
        return False

    accounts_list = com_object.NewObject("Массив")
    accounts_list.Add(acc)

    verbose_print(f"Загрузка остатков по счёту {ACCOUNT_CODE}...")

    try:
        params = {"Счета": accounts_list}
        results = execute_batch_query(com_object, QUERY_TEXT, COLUMNS, params=params)
        verbose_print(f"Найдено строк: {len(results)}")
    except Exception as e:
        verbose_print(f"Ошибка при загрузке остатков: {e}")
        import traceback
        verbose_print(traceback.format_exc())
        return False

    if not results:
        verbose_print("Остатки не найдены.")
        results = []

    for i, row in enumerate(results):
        parts = [
            str(row.get("Организация_UUID", "") or ""),
            str(row.get("Счет_UUID", "") or ""),
            str(row.get("Субконто1_UUID", "") or ""),
            str(row.get("Субконто2_UUID", "") or ""),
            str(row.get("Субконто3_UUID", "") or ""),
            str(row.get("СуммаДт", "") or ""),
            str(row.get("СуммаКт", "") or ""),
            str(i),
        ]
        row["row_uuid"] = hashlib.sha256("|".join(parts).encode()).hexdigest()

    rows = process_reference_fields(results, REFERENCE_COLUMNS)

    if not ensure_database_exists(sqlite_db_file):
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        return False

    try:
        cursor = connection.cursor()
        cursor.execute(f'DROP TABLE IF EXISTS "{TABLE_NAME}"')
        cursor.execute(f"""
            CREATE TABLE "{TABLE_NAME}" (
                row_uuid TEXT PRIMARY KEY,
                Организация TEXT,
                Счет TEXT,
                Субконто1 TEXT,
                Субконто2 TEXT,
                Субконто3 TEXT,
                ПодразделениеОрганизации TEXT,
                Валюта TEXT,
                ВидРБП_Представление TEXT,
                СуммаДт REAL,
                СуммаКт REAL
            )
        """)
        connection.commit()

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "row_uuid": "TEXT PRIMARY KEY",
                "Организация": "TEXT",
                "Счет": "TEXT",
                "Субконто1": "TEXT",
                "Субконто2": "TEXT",
                "Субконто3": "TEXT",
                "ПодразделениеОрганизации": "TEXT",
                "Валюта": "TEXT",
                "ВидРБП_Представление": "TEXT",
                "СуммаДт": "REAL",
                "СуммаКт": "REAL",
            },
        )
        verbose_print(f"Сохранено в SQLite: {saved} записей.")
        return True
    finally:
        connection.close()


if __name__ == "__main__":
    source = os.getenv("SOURCE_1C", "source")
    com = connect_to_1c(source)
    if com:
        load_account_01301_rbp_balances("BD/account_01301_rbp_balances.db", com)
