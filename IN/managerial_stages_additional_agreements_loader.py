def build_batch_main_query(mode: str, where_clause: str = "") -> str:
    """Строит текст пакетного запроса для основных данных."""
    limit_clause = ""
    if mode == "test":
        limit_clause = f" ПЕРВЫЕ {TEST_LIMIT}"
    return BATCH_MAIN_QUERY_TEMPLATE.format(
        limit_clause=limit_clause,
        where_clause=where_clause
    )


def build_batch_tabular_query(query_template: str, mode: str, where_clause: str = "") -> str:
    """Строит текст пакетного запроса для табличной части."""
    limit_clause = ""
    if mode == "test":
        limit_clause = f" ПЕРВЫЕ {TEST_LIMIT}"
    return query_template.format(
        limit_clause=limit_clause,
        where_clause=where_clause
    )


def _log(message: str) -> None:
    verbose_print(message)


def load_managerial_stages_additional_agreements(
    sqlite_db_file: str,
    com_object,
    mode: str = DEFAULT_MODE,
    process_func=None,
    filters_db: str | None = None,
) -> bool:
    _log("=" * 80)
    _log("ЗАГРУЗКА СПРАВОЧНИКА «УПРАВЛЕНЧЕСКИЕ ЭТАПЫ РАБОТ ДОПОЛНИТЕЛЬНЫХ СОГЛАШЕНИЙ»")
    _log("=" * 80)

    if com_object is None:
        _log("Ошибка: com_object обязателен")
        return False
    
    mode = mode or DEFAULT_MODE
    if mode not in ("test", "full"):
        _log(f"Неизвестный режим '{mode}'. Доступны: test, full.")
        return False

    where_clause = ""
    
    # Получаем фильтр по UUID, если есть
    filter_uuids = get_catalog_uuids(filters_db, TABLE_NAME)
    if filter_uuids:
        uuid_set = {value.strip().lower() for value in filter_uuids if value}
        _log(f"Применяется фильтр по UUID: {len(uuid_set)} записей")
        # Если есть фильтр, нужно использовать параметр &Ссылки
        # Но для этого нужно сначала получить ссылки по UUID
        # Пока оставляем пустое условие, фильтрацию сделаем после загрузки
        where_clause = ""

    limit_descr = (
        f"первые {TEST_LIMIT} записей" if mode == "test" else "полная выборка"
    )
    _log(f"\n[1/5] Подготовка пакетного запроса (режим: {mode.upper()} — {limit_descr})...")

    if not ensure_database_exists(sqlite_db_file):
        _log("Не удалось подготовить базу данных SQLite.")
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        _log("Не удалось подключиться к SQLite.")
        return False

    try:
        _log("Выполняем пакетный запрос...")
        
        # Строим пакетный запрос
        batch_query = build_batch_main_query(mode, where_clause)
        
        # Выполняем пакетный запрос
        raw_rows, raw_references = execute_batch_query(
            com_object,
            batch_query,
            MAIN_QUERY_COLUMNS,
            reference_attr="Ссылка",
            uuid_column="uuid",
        )
        
        if not raw_rows:
            _log("Предупреждение: справочник пуст или не найден в источнике.")
            return False

        # Применяем фильтр по UUID, если есть
        if filter_uuids:
            uuid_set = {value.strip().lower() for value in filter_uuids if value}
            filtered_rows = []
            filtered_references = []
            for i, row in enumerate(raw_rows):
                row_uuid = str(row.get("uuid", "") or "").strip().lower()
                if row_uuid and row_uuid in uuid_set:
                    filtered_rows.append(row)
                    if i < len(raw_references) and raw_references[i] and raw_references[i].get("reference"):
                        filtered_references.append(raw_references[i])
            raw_rows = filtered_rows
            raw_references = filtered_references
            _log(f"Применено ограничение по UUID: {len(raw_rows)} записей из {len(filter_uuids)}")

        if not raw_rows:
            _log("Не удалось получить записи справочника.")
            return False

        _log(f"\nВсего загружено записей: {len(raw_rows)}")

        rows = raw_rows
        if process_func:
            _log("\n[2/5] Обработка данных...")
            rows = process_func(rows)

        _log("\n[3/5] Сохранение в SQLite...")
        # Обрабатываем поля-ссылки и перечисления
        rows = process_reference_fields(rows, REFERENCE_COLUMNS)

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "uuid": "TEXT PRIMARY KEY",
                "Ссылка": "TEXT",
                "Код": "TEXT",
                "Наименование": "TEXT",
            },
        )
        _log(f"Сохранено строк: {saved}")
        
        # Загружаем табличные части одним запросом
        _log("\n[4/5] Загрузка табличных частей...")
        for section_info in BATCH_TABULAR_QUERIES:
            section_name = section_info["name"]
            table_name = section_info["table"]
            columns = section_info["columns"]
            base_columns = section_info["base_columns"]
            reference_columns = section_info.get("reference_columns", [])
            query_template = section_info["query_template"]
            
            _log(f"  Загрузка табличной части '{section_name}'...")
            
            # Создаем таблицу, если её нет
            ensure_table_schema(connection, table_name, columns, base_columns)
            
            try:
                # Строим пакетный запрос для табличной части
                batch_query = build_batch_tabular_query(query_template, mode, where_clause)
                
                # Выполняем пакетный запрос
                tabular_rows = execute_batch_query(
                    com_object,
                    batch_query,
                    columns,
                )
                
                if not tabular_rows:
                    _log(f"  Табличная часть '{section_name}': нет данных")
                    continue
                
                # Обрабатываем ссылочные поля
                import json
                for row in tabular_rows:
                    for column in reference_columns:
                        presentation_key = f"{column}_Представление"
                        uuid_key = f"{column}_UUID"
                        type_key = f"{column}_Тип"
                        presentation = row.pop(presentation_key, "")
                        uuid_value = row.pop(uuid_key, "")
                        type_value = row.pop(type_key, "")
                        if presentation or uuid_value or type_value:
                            row[column] = json.dumps(
                                {
                                    "presentation": presentation,
                                    "uuid": uuid_value,
                                    "type": type_value,
                                },
                                ensure_ascii=False,
                            )
                        else:
                            row[column] = ""
                
                # Сохраняем строки
                saved_count = upsert_rows(connection, table_name, tabular_rows, base_columns)
                _log(f"  Сохранено строк табличной части '{section_name}': {saved_count}")
                    
            except Exception as e:
                error_msg = str(e)
                if "Таблица не найдена" in error_msg or "не найдена" in error_msg.lower():
                    _log(f"  ⚠ Табличная часть '{section_name}' не найдена в источнике, пропускаем")
                else:
                    _log(f"  Ошибка загрузки табличной части '{section_name}': {e}")
                    import traceback
                    _log(f"  Детали ошибки:\n{traceback.format_exc()}")
            
    finally:
        connection.close()

    _log("\n[5/5] Загрузка завершена успешно!")
    return True
