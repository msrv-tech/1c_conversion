def build_main_query_text(mode: str) -> str:
    """Строит текст запроса (лимит только в test-режиме)."""
    limit_clause = ""
    if mode == "test":
        limit_clause = f"    ПЕРВЫЕ {TEST_LIMIT}\n"
    
    query = MAIN_QUERY_TEMPLATE.format(limit_clause=limit_clause)
    return query


def _log(message: str) -> None:
    verbose_print(message)


def load_supplier_orders(
    sqlite_db_file: str,
    com_object,
    mode: str = DEFAULT_MODE,
    process_func=None,
    filters_db: str | None = None,
) -> bool:
    """
    Загружает документы "Заказ поставщику" из 1С источника в SQLite.
    
    Args:
        sqlite_db_file: Путь к файлу базы данных SQLite
        com_object: COM-объект подключения к 1С (источник)
        mode: Режим загрузки ("test" или "full")
        process_func: Функция для предварительной обработки данных (опционально)
        filters_db: Путь к БД с фильтрами по UUID (опционально)
    
    Returns:
        True если успешно, False если ошибка
    """
    _log("=" * 80)
    _log("ЗАГРУЗКА ДОКУМЕНТОВ «ЗАКАЗ ПОСТАВЩИКУ»")
    _log("=" * 80)

    if com_object is None:
        _log("Ошибка: com_object обязателен")
        return False
    
    mode = mode or DEFAULT_MODE
    if mode not in ("test", "full"):
        _log(f"Неизвестный режим '{mode}'. Доступны: test, full.")
        return False

    limit_descr = (
        f"первые {TEST_LIMIT} записей" if mode == "test" else "полная выборка"
    )
    _log(f"\n[1/4] Подготовка запроса (режим: {mode.upper()} — {limit_descr})...")
    
    _log("\n[2/4] Чтение документов ЗаказПоставщику...")
    
    try:
        query_text = build_main_query_text(mode)
        params = None
        _log("Отбор: все заказы (без ограничения по датам)")
        
        rows = execute_query(
            com_object,
            query_text,
            MAIN_QUERY_COLUMNS,
            params=params,
        )
        _log(f"Получено строк из execute_query: {len(rows) if rows else 0}")
    except Exception as error:
        _log(f"Ошибка выполнения запроса: {error}")
        import traceback
        _log(traceback.format_exc())
        return False

    # Один заказ может дать несколько строк из-за ЛЕВОГО СОЕДИНЕНИЯ с customСубподрядныеАктыАсуп; оставляем по одной записи на заказ
    seen_uuids = set()
    unique_rows = []
    for row in rows:
        row_uuid = (str(row.get("ЗаказПоставщику_UUID", "") or "").strip().lower())
        if row_uuid and row_uuid not in seen_uuids:
            seen_uuids.add(row_uuid)
            unique_rows.append(row)
    if len(unique_rows) < len(rows):
        _log(f"Дедупликация по заказу: {len(unique_rows)} записей (было строк {len(rows)})")
    rows = unique_rows

    filter_uuids = get_catalog_uuids(filters_db, TABLE_NAME)
    if filter_uuids:
        uuid_set = {value.strip().lower() for value in filter_uuids if value}
        filtered_rows = []
        for row in rows:
            row_uuid = str(row.get("ЗаказПоставщику_UUID", "") or "").strip().lower()
            if row_uuid and row_uuid in uuid_set:
                filtered_rows.append(row)
        rows = filtered_rows
        _log(f"Применено ограничение по UUID: {len(rows)} записей из {len(filter_uuids)}")

    if not rows:
        _log("Не удалось получить записи документов.")
        return False

    _log(f"Получено записей: {len(rows)}")

    if process_func:
        _log("\n[3/4] Обработка данных...")
        rows = process_func(rows)

    _log("\n[4/4] Сохранение в SQLite...")
    if not ensure_database_exists(sqlite_db_file):
        _log("Не удалось подготовить базу данных SQLite.")
        return False

    connection = connect_to_sqlite(sqlite_db_file)
    if not connection:
        _log("Не удалось подключиться к SQLite.")
        return False

    try:
        # ВАЖНО: Сохраняем UUID ДО обработки ссылочных полей, чтобы он не был потерян
        for row in rows:
            if "ЗаказПоставщику_UUID" in row and row["ЗаказПоставщику_UUID"]:
                row["uuid"] = str(row["ЗаказПоставщику_UUID"]).strip()
        
        # Обрабатываем поля-ссылки и перечисления
        rows = process_reference_fields(rows, REFERENCE_COLUMNS)
        
        # Убеждаемся, что UUID сохранен (на случай, если process_reference_fields его удалил)
        for row in rows:
            if "uuid" not in row or not row.get("uuid"):
                if "ЗаказПоставщику_UUID" in row and row["ЗаказПоставщику_UUID"]:
                    row["uuid"] = str(row["ЗаказПоставщику_UUID"]).strip()

        saved = upsert_rows(
            connection,
            TABLE_NAME,
            rows,
            {
                "uuid": "TEXT PRIMARY KEY",
                "ЗаказПоставщику_UUID": "TEXT",
                "Номер": "TEXT",
                "Дата": "TEXT",
            },
        )
        _log(f"Сохранено строк: {saved}")
    finally:
        connection.close()

    return True

